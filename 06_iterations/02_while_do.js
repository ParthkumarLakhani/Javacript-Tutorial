/*

| While Type              | Description                                      |
|-------------------------|--------------------------------------------------|
| Basic while             | Runs while condition is true                     |
| Infinite while          | Runs forever until break                         |
| while + break           | Manual exit logic                                |
| while + continue        | Skip some iterations                             |
| do...while              | Runs at least once                               |
| No-body while           | One-line loops                                   |
| Multi-condition while   | Use multiple variables inside loop               |

*/




